# 1번 문제 =====================================================
x = int(input("첫 번째 정수를 입력하세요 : "))
y = int(input("두 번째 정수를 입력하세요 : "))

# 두 수의 합
add = x + y
print(f"두 수의 합 : {add}")

# 두 수의 차
sub = x - y
print(f"두 수의 차 : {sub}")

# 두 수의 곱
mul = x * y
print(f"두 수의 곱 : {mul}")

# 두 수의 평균
avg = (x + y) / 2
print(f"두 수의 평균 : {avg}")

# 큰 수
max_num = max(x, y)
print(f"큰 수 : {max_num}")

# 작은 수수
min_num = min(x, y)
print(f"작은 수 : {min_num}")