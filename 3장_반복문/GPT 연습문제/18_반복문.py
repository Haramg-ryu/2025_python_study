# 구구단 전체(2단~9단)를 표 형식으로 출력하시오.

# 출력 예시:
# 2*1=2   3*1=3   4*1=4 ... 9*1=9
# 2*2=4   3*2=6   4*2=8 ... 9*2=18
# ...
# 2*9=18  3*9=27 ... 9*9=81

# ------------------------------------------

for i in range(1, 10):      # 곱해지는 수
    for j in range(2, 10):  # 단
        print(f"{j} * {i:<2d} = {i * j:2d}  ", end="| ")
    print()